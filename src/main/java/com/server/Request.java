package com.server;

public class Request {
    private String key;
    private String data;

    public String getKey() {
        return key;
    }
    public String getData() {
        return data;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public void setData(String data) {
        this.data = data;
    }
}
